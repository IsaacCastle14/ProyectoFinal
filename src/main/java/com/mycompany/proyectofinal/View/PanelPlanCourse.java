    public void inicializarComboBoxStudyProgram(List<StudyProgramModel> elementosList) {
        cbxStudyPlan.removeAllItems();

        for (StudyProgramModel studyProgram : elementosList) {
            if (studyProgram.getCursos().isEmpty()) {
                cbxStudyPlan.addItem(studyProgram.getName());
            }
        }
    }

